# Tipe data adalah macam macam data yang bisa di masukan kedalam variable
# Contoh: a = 10, a adalah variabel dengan nilai 10

# Tipe data: Angka satuan atau tidak mengandung koma (integer)
data_integer = 150000
print("data : ", (data_integer))
print("- bertipe ", type(data_integer))

# Tipe data: Angka dengan koma atau Desimal (FLOAT)
data_float = 2.3
print("data : ", (data_float))
print("- bertipe ", type(data_float))

# Tipe data: Kumpulan karakter (string)
data_string = "Johnny"
print("data : ", (data_string))
print("- bertipe ", type(data_string))

# Tipe data: biner true/false (Boolean)
data_bool = False
print("data : ", (data_bool))
print("- bertipe ", type(data_bool))

